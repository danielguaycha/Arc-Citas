    private void agregarOyente() {     
      r_dateReserv.getJCalendar().getDayChooser().addPropertyChangeListener(
              
             //Cita c = new Cita(WIDTH, WIDTH, null, TOOL_TIP_TEXT_KEY, TOOL_TIP_TEXT_KEY, WIDTH)
              
                new java.beans.PropertyChangeListener() {
                    @Override
                    public void propertyChange(java.beans.PropertyChangeEvent evt) {
                        if (evt.getPropertyName().compareTo("day") == 0) {
                            try {
                               SimpleDateFormat formatoDeFecha = new SimpleDateFormat("yyyy-MM-dd");
                                //System.out.println(formatoDeFecha.format(r_dateReserv.getJCalendar().getDate()));
                                cr.cargar_horas_by_fecha(formatoDeFecha.format(r_dateReserv.getJCalendar().getDate()));
                            } catch (Exception ex) {
                                Logger.getLogger(p_reserv.class.getName()).log(Level.SEVERE, null, ex);
                            }
                        }   
                    }
                });
    }
