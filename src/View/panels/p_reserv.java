    private void r_btnReservarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_r_btnReservarActionPerformed
        int id_paciente = (r_txtID.getText().equals(""))?0:Integer.parseInt(r_txtID.getText());
        if(id_paciente>0)
        {
            int id_especialidad = r_selecEspecialidad.getSelectedIndex();
            Date f = r_dateReserv.getDate();
            String hora = (r_list_hour_reserv.getSelectedValue()==null)?"":r_list_hour_reserv.getSelectedValue().toString();
            double costo =(r_txtcosto.getText().equals(""))?0:Double.parseDouble(r_txtcosto.getText());
            String cedula = txtCedula.getText();
            
            String []notify = cr.reservar(id_especialidad, new java.sql.Date(f.getTime()), hora, costo, id_paciente,cedula).split(";");
           
            notifyController.setNotify(notify[1], notify[0]);
        }else{
            notifyController.setNotify("Es necesario escojer un Paciente o Registrando...", "Alert");
        }   
    }//GEN-LAST:event_r_btnReservarActionPerformed

    private void r_btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_r_btn_saveActionPerformed
        String ced = p_reserv.txtCedula.getText();
        String nom = p_reserv.r_txtNombre.getText();
        String ape = p_reserv.r_txtApellido.getText();
        String email = p_reserv.r_txtEmail.getText();
        String dir = p_reserv.r_txtDir.getText();
        String estado_civil = p_reserv.r_selectEstad_cib.getSelectedItem().toString();
        String cel = p_reserv.r_txtCelular.getText();  
        String pass = r_txtPass.getText();
        Paciente p = new Paciente(ced, nom, ape, email, dir, estado_civil, cel,pass);
        Control_paciente cp = new Control_paciente();
        String []notify = cp.validar_datos(p, 1).split(";");
        
        notifyController.setNotify(notify[1], notify[0]);
        if(notify[0].equals("Ok")){
            cr.enabledCampos(false);
        }
        r_btnEdit.setEnabled(false);
        r_txtID.setText(cp.get_id_by_cedula(ced)+"");
        
    }//GEN-LAST:event_r_btn_saveActionPerformed

    private void r_selecEspecialidadItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_r_selecEspecialidadItemStateChanged
        int id_especializacion = r_selecEspecialidad.getSelectedIndex();
        if(id_especializacion>0)
        {
            cr.load_doc_by_area(id_especializacion);
            if(r_selecEspecialidad.getSelectedIndex()>0){
                r_selecDoctor.setEnabled(true);
            }else{
                r_selecDoctor.setEnabled(false);
                r_dateReserv.getJCalendar().setEnabled(false);
                  r_dateReserv.getCalendarButton().setEnabled(false);
            }
        }
         
    }//GEN-LAST:event_r_selecEspecialidadItemStateChanged

