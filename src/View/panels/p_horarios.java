    private void h_btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_h_btnActualizarActionPerformed
        int id_doc =(h_id.getText().equals(""))?0:Integer.parseInt(h_id.getText());
        if(id_doc!=0){
        String hora_in = h_txtHoraIni.getText();
        String hora_out = h_txthoraFin.getText();
        String duracion = h_txtDuracion.getText();
        String []notify = ch.validar_datos(id_doc,hora_in,hora_out,duracion,2).split(";");
        notifyController.setNotify(notify[1], notify[0]);
        
        }else{
            notifyController.setNotify("Debes seleccionar un doctor :D", "Alert");
        }
    }//GEN-LAST:event_h_btnActualizarActionPerformed

