    private void h_slide_minStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_h_slide_minStateChanged
       if(txt!=null){
        String []time = txt.getText().split(":");
        int val = h_slide_min.getValue();
        if(val>9){
        txt.setText(time[0]+":"+val+":00");}
        else{
         txt.setText(time[0]+":0"+val+":00");
        }
       } 
    }//GEN-LAST:event_h_slide_minStateChanged

    private void h_txtbusquedaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_h_txtbusquedaFocusLost
       String ced = h_Busqueda_horario.getText();
       if(!ced.equals(""))
       {
           String []notify = ch.validar_horario(ced).split(";");
           if(notify.length==2){
           notifyController.setNotify(notify[1], notify[0]);}
       }
    }//GEN-LAST:event_h_txtbusquedaFocusLost

    private void h_btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_h_btnGuardarActionPerformed
        int id_doc =(h_id.getText().equals(""))?0:Integer.parseInt(h_id.getText());
        String hora_in = h_txtHoraIni.getText();
        String hora_out = h_txthoraFin.getText();
        String duracion = h_txtDuracion.getText();
        if(id_doc>0){
        String []notify = ch.validar_datos(id_doc,hora_in,hora_out,duracion,1).split(";");
        notifyController.setNotify(notify[1], notify[0]);}
    }//GEN-LAST:event_h_btnGuardarActionPerformed

