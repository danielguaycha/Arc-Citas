    private void menuMoved(JLabel j){
        j.setOpaque(true);
        j.setBackground(new Color(50,60,75));
        j.setForeground(Color.white);
        j.setBorder(BorderFactory.createEmptyBorder(5, 5, 5, 5));
        
    }
    private void menuOut(JLabel j){
        j.setOpaque(false);
        j.setForeground(new Color(102,102,102));
        j.setBorder(BorderFactory.createEmptyBorder(0,0,0,0));
    }
    
